// Base component in the component system
#include "component.h"
#include "sceneNode.h"

namespace rev { namespace game {

	//----------------------------------------------------------------------------------------------------------------------
	Component::~Component() {
		dettach();
	}

	//----------------------------------------------------------------------------------------------------------------------
	void Component::attachTo(SceneNode* _node) {
		mNode = _node;
	}

	//----------------------------------------------------------------------------------------------------------------------
	void Component::dettach() {
		if(mNode) {
			mNode = nullptr;
		}
	}

}}	// namespace rev::game